package text3_1;

public abstract class Abstract_Instruments {
    //打印字符串
    public abstract void print_str(String s);
    //将字符串中大写字母全部转换为小写
    public abstract String toCapital(String s);
    //计算加法结果并打印
    public abstract void add(int a, int b);
    //计算圆的面积并打印
    public abstract void circle_area(int i);
    //打印出一个矩阵
    public abstract void matrix();
}